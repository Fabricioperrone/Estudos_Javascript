/**
 * operadores de atribuição
 * atribuem um valor a uma variável
 */
var color = 'beige';
console.log(color)

/**
 * operadores aritméticos
 * executam matemática básica
 */
area = 3 * 2;
console.log(area)

/** 
 * operadores de string
 * combinam duas strings
 */
greeting = 'Hi, ' + 
'Molly'
console.log(greeting)

/** 
 * Operadores de comparação
 * Comparam dois valores e retornam true ou false
 */
buy = 3 > 5
console.log(buy)

/**
 * operadores lógicos
 * combinam expressões e retornam true ou false
 */
buy = (5 > 3) && (2 < 4)
console.log(buy)

