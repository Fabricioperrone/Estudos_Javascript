// Desafio de PI

const PI = 3.141532;
const raio = 10;
let areaCircunferencia = PI * raio * raio;

console.log("O valor da área é: " + areaCircunferencia + "m2.");

// usando o Math.PI do Js
areaCircunferencia = Math.PI *  raio * raio;
console.log("O valor da área é: " + areaCircunferencia + " m2.");